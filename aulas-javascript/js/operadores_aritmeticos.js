//Operadores Aritmeticos

//multiplicação *
console.log(14 * 20)
console.log(4.02 * 50)
console.log(1.2 * 5.6)

//divisão /
console.log(13 / 20)

//soma +
console.log(23 + 100)


//subtração -
console.log(52 - 30)


//resto da divisão %
let remainder
remainder = 20 % 3
console.log(remainder)


//incremento ++
let increment = 0
increment++
increment++
increment++
console.log(increment)

console.log(++increment)
console.log(increment++)
